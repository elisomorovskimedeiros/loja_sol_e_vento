class SlidesIndex{
    constructor(figura1, figura2, figura3){
        this.figura1 = figura1,
        this.figura2 = figura2,
        this.figura3 = figura3
    }

    get figura1(){
        return this.figura1;
    }
    get figura2(){
        return this.figura2;
    }
    get figura3(){
        return this.figura3;
    }
    set figura1(figura1){
        this.figura1 = figura1;
    }
    set figura2(){
        this.figura2 = figura2;
    }
    set figura3(){
        this.figura3 = figura3;
    } 
}